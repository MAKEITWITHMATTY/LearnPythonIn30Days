a = 10
b = 3

print(a + b)   # Addition        → 13
print(a - b)   # Subtraction     → 7
print(a * b)   # Multiplication  → 30
print(a / b)   # True division   → 3.3333333333333335 (a float)
print(a // b)  # Floor division  → 3   (drops the fractional part)
print(a % b)   # Modulus         → 1   (the remainder of 10 ÷ 3)
print(a ** b)  # Exponentiation  → 1000 (10 to the power of 3)
